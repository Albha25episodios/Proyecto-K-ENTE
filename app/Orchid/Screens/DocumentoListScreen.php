<?php

namespace App\Orchid\Screens;

use App\Models\Tabla_raiz;
use App\Orchid\Layouts\DocumentoListLayout;
use Orchid\Screen\Actions\Link;
use Orchid\Screen\Screen;

class DocumentoListScreen extends Screen
{
    /**
     * Fetch data to be displayed on the screen.
     *
     * @return array
     */
    public function query(): iterable
    {
        return [
            'documentos' => Tabla_raiz::with(['castellano.quechua', 'castellano.aymara'])->paginate(),
        ];
    }

    /**
     * The name of the screen displayed in the header.
     *
     * @return string|null
     */
    public function name(): ?string
    {
        return 'DocumentoListScreen';
    }

    /**
     * The description is displayed on the user's screen under the heading
     */
    public function description(): ?string
    {
        return "All blog posts";
    }

    /**
     * The screen's action buttons.
     *
     * @return \Orchid\Screen\Action[]
     */
    public function commandBar(): iterable
    {
        return [
            Link::make('Create new')
                ->icon('pencil')
                ->route('platform.documento.create')
        ];
    }

    /**
     * The screen's layout elements.
     *
     * @return \Orchid\Screen\Layout[]|string[]
     */
    public function layout(): iterable
    {
        return [
          DocumentoListLayout::class
        ];
    }
}
